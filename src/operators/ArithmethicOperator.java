package operators;

public class ArithmethicOperator 
{

	int addTwoNumber(int a,int b)
	{
	return a+b;
	}
	int subTwoNumber(int a,int b)
	{
	return a-b;
	}
	int mulTwoNumber(int a,int b)
	{
	return a*b;
	}
	public double divTwoNumber(int a,int b)
	{
	return a/b;
	}
	int modTwoNumber(int a,int b)
	{
	return a%b;
	}

}
