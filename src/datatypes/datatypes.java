package datatypes;

public class datatypes 
{
byte b=10;
short s=20;
int i=250;
long l=985854854252L;
float f=2.5f;
double d=8.23506253d;
boolean o=true;
char c='a';
public static void main(String[] types)
{
datatypes datatypes=new datatypes();
System.out.println("byte number::::"+datatypes.b);
System.out.println("Short number::::"+datatypes.s);
System.out.println("Int number:::::"+datatypes.i);
System.out.println("Long number:::::"+datatypes.l);
System.out.println("float number:::::"+datatypes.f);
System.out.println("double number:::::"+datatypes.d);
System.out.println("Boolean number:::::"+datatypes.o);
System.out.println("char letter:::::"+datatypes.c);

}
}